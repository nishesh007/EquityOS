function dedupe(symbols: readonly string[]): string[] {
  return [...new Set(symbols.map((s) => s.toUpperCase()))];
}

export function getNifty50(): string[] {
  return dedupe(NIFTY_50);
}

export function getNifty100(): string[] {
  return dedupe([...NIFTY_50, ...NIFTY_100_EXTRA]);
}

export function getNifty200(): string[] {
  return dedupe([...getNifty100(), ...NIFTY_200_EXTRA]);
}

export function getNifty500(): string[] {
  return dedupe([...getNifty200(), ...NIFTY_500_EXTRA]);
}
